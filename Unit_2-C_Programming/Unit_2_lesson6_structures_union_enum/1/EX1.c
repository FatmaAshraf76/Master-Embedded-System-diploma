#include <stdio.h>
struct information
{
	char name[50];
	int roll;
	float marks;
};
int main(void)
{
	struct information s1;
	printf("Enter information of students:\n");
	printf("Enter name: ");
	fflush(stdout);
	gets(s1.name);
	fflush(stdout);
	printf("Enter roll number: ");
	fflush(stdout);
	scanf("%d",&s1.roll);
	fflush(stdout);
	printf("Enter marks: ");
	fflush(stdout);
	scanf("%f",&s1.marks);
	fflush(stdout);
	printf("Displaying information\n");
	printf("Name: %s \n",s1.name);
	printf("Roll: %d \n",s1.roll);
	printf("Marks: %f \n",s1.marks);
	return 0;
}
